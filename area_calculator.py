# Hello there!
# This program will calculate the area of a triangle, rectangle and circle.

import math

# Calculating the area of a triangle:

base = 20
height = 30
area = base * height / 2

print("The triangle area is", area)

# Calculcating the area of a rectangle:

length = 2
width = 12
area = length * width

print("The rectangle area is", area)

#Calculating the area of a circle:

radius = 36
area = math.pi * radius * radius
    
print("The circle area is", area)
